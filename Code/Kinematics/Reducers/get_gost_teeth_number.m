function z_gost = get_gost_teeth_number(m, z_desired)
% GET_GOST_TEETH_NUMBER Возвращает ГОСТ-стандартизированное число зубьев
%   z_gost = GET_GOST_TEETH_NUMBER(m, z_desired)
%
%   Входные параметры:
%       m - модуль (мм) из ряда ГОСТ 6033-80
%       z_desired - желаемое число зубьев
%
%   Выходные параметры:
%       z_gost - стандартизированное число зубьев по ГОСТ
%
%   Пример:
%       z = get_gost_teeth_number(2, 25)  % Вернёт 26

