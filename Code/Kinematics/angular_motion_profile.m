function [alpha, omega_max, t] = angular_motion_profile(T, theta, number)
% ANGULAR_MOTION_PROFILE - Расчет углового ускорения для движения
% с нулевыми начальной и конечной скоростями
%
% Вход:
%   T     - время движения [с]
%   theta - конечный угол [рад]
%
% Выход:
%   alpha     - требуемое угловое ускорение [рад/с²]
%   omega_max - максимальная угловая скорость [рад/с]
%   t         - вектор времени для построения графиков
    
    % Расчет ускорения (формула для симметричного треугольника)
    %alpha = 4 * theta / T^2;

    % Расчет ускорения (формула для симметричной трапеции)
    alpha = 64 * theta / (T^2 * 7);
    
    % Максимальная скорость в середине интервала (треугольник)
    %omega_max = 2 * theta / T;

    % Максимальная скорость в середине интервала (трапеция)
    omega_max = 8 * theta / (T * 7);
    
    % Вектор времени для визуализации
    t = linspace(0, T, 1000);
    
    % Вывод результатов
    fprintf('\n=== Результаты расчета %.0f звена ===\n', number);
    fprintf('Время движения:      %.3f с\n', T);
    fprintf('Конечный угол:       %.3f рад (%.2f°)\n', theta, rad2deg(theta));
    fprintf('Угловое ускорение:   %.3f рад/с²\n', alpha);
    fprintf('Макс. угловая скор.: %.3f рад/с (%.2f об/мин)\n', ...
omega_max, omega_max * 60 / (2*pi));
end
